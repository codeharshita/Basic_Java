package JavaBasics;

public class StringConcatenation {

	public static void main(String[] args) {
		int i = 10;
		int j=52;
		
		String x="Hello";
		String y="World";
		
		System.out.println(i+j);
		
		System.out.println(i+j+x+y);
		
		System.out.println(x+y+i+j);
		
		System.out.println(x+y+(i+j));
		
		

	}

}
